class TeamEntity:
    __score = None
    __title = None
    __country = None

    def setScore(self, score):
        self.__score = score

    def getScore(self):
        return self.__score

    def setTitle(self, title):
        self.__title = title

    def getTitle(self):
        return self.__title

    def setCountry(self, country):
        self.__country = country

    def getCountry(self):
        return self.__country